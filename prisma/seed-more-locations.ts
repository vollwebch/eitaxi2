async function main() {
  console.log('🌱 Agregando más ubicaciones...')
  let added = 0

  for (const loc of moreLocations) {
    try {
      await prisma.location.upsert({
        where: {
          name_type_postalCode: {
            name: loc.name,
            type: 'city',
            postalCode: loc.postalCode
          }
        },
        update: {
          latitude: loc.lat,
          longitude: loc.lng,
        },
        create: {
          name: loc.name,
          type: 'city',
          postalCode: loc.postalCode,
          latitude: loc.lat,
          longitude: loc.lng,
          canton: { connect: { code: loc.canton } }
        }
      })
      added++
      if (added % 100 === 0) {
        console.log(`  ✓ ${added} ubicaciones agregadas...`)
      }
    } catch (e) {
      // Ignorar duplicados
    }
  }

  console.log(`\n✅ Completado! ${added} ubicaciones agregadas`)
  
  const total = await prisma.location.count()
  console.log(`   Total en base de datos: ${total}`)
}

