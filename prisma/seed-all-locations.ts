async function main() {
  console.log('🌱 Iniciando seed de todos los códigos postales...')
  console.log('📍 Esto puede tomar unos minutos...\n')

  let totalLocations = 0
  let totalCantons = 0

  // Crear/actualizar cantones suizos
  console.log('📍 Creando cantones suizos...')
  for (const cantonCode of Object.keys(swissPostalCodes)) {
    try {
      await prisma.location.upsert({
        where: {
          name_type_postalCode: {
            name: cantonCode,
            type: 'canton',
            postalCode: null
          }
        },
        update: { },
        create: {
          name: cantonCode,
          type: 'canton',
          canton: { connect: { code: cantonCode } }
        }
      })
      totalCantons++
    } catch (e) {
      // Ignorar errores de cantones
    }
  }
  console.log(`  ✓ ${totalCantons} cantones procesados\n`)

  // Crear ubicaciones por cantón
  console.log('🏙️ Creando ubicaciones con códigos postales...')
  for (const [cantonCode, locations] of Object.entries(swissPostalCodes)) {
    console.log(`  Procesando ${cantonCode}...`)
    
    for (const loc of locations) {
      try {
        await prisma.location.upsert({
          where: {
            name_type_postalCode: {
              name: loc.name,
              type: 'city',
              postalCode: loc.postalCode
            }
          },
          update: {
            latitude: loc.lat,
            longitude: loc.lng,
          },
          create: {
            name: loc.name,
            type: 'city',
            postalCode: loc.postalCode,
            latitude: loc.lat,
            longitude: loc.lng,
            canton: { connect: { code: cantonCode } }
          }
        })
        totalLocations++
      } catch (e: any) {
        // Ignorar duplicados
        if (!e.message?.includes('Unique constraint')) {
          console.log(`    ⚠️ Error con ${loc.name}: ${e.message}`)
        }
      }
    }
    console.log(`    ✓ ${locations.length} ubicaciones`)
  }

  // Crear ubicaciones de Liechtenstein
  console.log('\n🏛️ Creando ubicaciones de Liechtenstein...')
  for (const loc of liechtensteinPostalCodes) {
    try {
      await prisma.location.upsert({
        where: {
          name_type_postalCode: {
            name: loc.name,
            type: 'city',
            postalCode: loc.postalCode
          }
        },
        update: {
          latitude: loc.lat,
          longitude: loc.lng,
        },
        create: {
          name: loc.name,
          type: 'city',
          postalCode: loc.postalCode,
          latitude: loc.lat,
          longitude: loc.lng,
          canton: { connect: { code: 'LI' } }
        }
      })
      totalLocations++
    } catch (e: any) {
      if (!e.message?.includes('Unique constraint')) {
        console.log(`  ⚠️ Error con ${loc.name}: ${e.message}`)
      }
    }
  }
  console.log(`  ✓ ${liechtensteinPostalCodes.length} ubicaciones de Liechtenstein`)

  console.log(`\n✅ Seed completado!`)
  console.log(`   📍 ${totalCantons} cantones`)
  console.log(`   🏙️ ${totalLocations} ubicaciones totales`)
  console.log(`   📊 ${Object.keys(swissPostalCodes).length} cantones suizos + Liechtenstein`)
}

main()
  .catch((e) => {
    console.error('❌ Error en seed:', e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
