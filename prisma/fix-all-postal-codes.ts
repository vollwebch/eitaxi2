async function main() {
  console.log('🔧 Actualizando TODOS los códigos postales...')
  console.log('='.repeat(60))
  
  let updated = 0
  let notFound = 0
  
  // First, get all cities without postal codes
  const citiesWithoutPostal = await prisma.city.findMany({
    where: { postalCode: null },
    include: { canton: true }
  })
  
  console.log(`📊 Municipios sin código postal: ${citiesWithoutPostal.length}`)
  
  for (const city of citiesWithoutPostal) {
    // Try to find by exact name
    let postalCode = postalCodeMap[city.name]
    
    // Try with canton suffix if not found
    if (!postalCode) {
      postalCode = postalCodeMap[`${city.name} (${city.canton.code})`]
    }
    
    // Try variations
    if (!postalCode) {
      // Try without special characters
      const normalizedName = city.name.replace(/[^a-zA-ZÀ-ÿ\s]/g, '').trim()
      postalCode = postalCodeMap[normalizedName]
    }
    
    if (postalCode) {
      await prisma.city.update({
        where: { id: city.id },
        data: {
          postalCode: postalCode,
          updatedAt: new Date()
        }
      })
      updated++
      if (updated % 100 === 0) {
        console.log(`📊 Progreso: ${updated} actualizados...`)
      }
    } else {
      notFound++
    }
  }
  
  console.log('')
  console.log('='.repeat(60))
  console.log('✅ ACTUALIZACIÓN COMPLETADA!')
  console.log('='.repeat(60))
  console.log(`📊 Códigos postales actualizados: ${updated}`)
  console.log(`📊 No encontrados: ${notFound}`)
  
  const withPostal = await prisma.city.count({
    where: { postalCode: { not: null } }
  })
  const total = await prisma.city.count()
  
  console.log(`\n📈 Municipios con código postal: ${withPostal}/${total}`)
}

main()
  .catch((e) => {
    console.error('❌ Error:', e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
