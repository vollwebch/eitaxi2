async function main() {
  console.log('🌱 Starting comprehensive seed...')
  
  // Borrar datos existentes
  await prisma.driverRoute.deleteMany()
  await prisma.driverSchedule.deleteMany()
  await prisma.taxiDriver.deleteMany()
  await prisma.place.deleteMany()
  await prisma.city.deleteMany()
  await prisma.canton.deleteMany()
  await prisma.route.deleteMany()
  
  console.log('✅ Cleared existing data')

  // Crear cantones y ciudades
  for (const region of allRegions) {
    const canton = await prisma.canton.create({
      data: {
        name: region.name,
        code: region.code,
        slug: region.slug,
        latitude: region.latitude,
        longitude: region.longitude,
      },
    })
    
    console.log(`✅ Created canton: ${region.name}`)
    
    // Crear ciudades
    for (const city of (region as any).cities || []) {
      await prisma.city.create({
        data: {
          name: city.n,
          slug: city.s,
          latitude: city.lat,
          longitude: city.lng,
          postalCode: city.pc,
          cantonId: canton.id,
        },
      })
    }
    
    console.log(`  ✅ Created ${(region as any).cities?.length || 0} cities for ${region.name}`)
  }

  console.log('🎉 Comprehensive seed completed!')
  console.log(`📊 Total cantons: ${await prisma.canton.count()}`)
  console.log(`📊 Total cities: ${await prisma.city.count()}`)
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
