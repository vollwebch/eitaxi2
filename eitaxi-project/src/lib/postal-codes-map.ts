// Mapeo inverso: código postal -> ciudades que lo incluyen
export const POSTCODE_TO_CITIES: Record<string, string[]> = {};

// Construir mapeo inverso automáticamente
for (const [city, postcodes] of Object.entries(CITY_TO_POSTCODES)) {
  for (const postcode of postcodes) {
    if (!POSTCODE_TO_CITIES[postcode]) {
      POSTCODE_TO_CITIES[postcode] = [];
    }
    if (!POSTCODE_TO_CITIES[postcode].includes(city)) {
      POSTCODE_TO_CITIES[postcode].push(city);
    }
  }
}

/**
 * Normaliza un nombre de ciudad para buscar en el mapeo
 */
export function normalizeCityName(city: string): string {
  return city
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')  // Eliminar diacríticos
    .replace(/ä/g, 'ae').replace(/ö/g, 'oe').replace(/ü/g, 'ue')
    .replace(/[\.\-\_]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

/**
 * Obtiene los códigos postales asociados a una ciudad
 */
export function getPostcodesForCity(cityName: string): string[] {
  const normalized = normalizeCityName(cityName);
  return CITY_TO_POSTCODES[normalized] || [];
}

/**
 * Obtiene las ciudades asociadas a un código postal
 */
export function getCitiesForPostcode(postcode: string): string[] {
  return POSTCODE_TO_CITIES[postcode] || [];
}

/**
 * Verifica si un código postal está asociado a una ciudad
 */
export function postcodeMatchesCity(postcode: string, cityName: string): boolean {
  const normalizedCity = normalizeCityName(cityName);
  const cities = getCitiesForPostcode(postcode);
  return cities.some(c => c === normalizedCity);
}

/**
 * Verifica si una ciudad está asociada a un código postal
 */
export function cityMatchesPostcode(cityName: string, postcode: string): boolean {
  const postcodes = getPostcodesForCity(cityName);
  return postcodes.includes(postcode);
}

/**
 * Verifica si dos nombres de ciudades son equivalentes
 */
export function citiesAreEquivalent(city1: string, city2: string): boolean {
  const norm1 = normalizeCityName(city1);
  const norm2 = normalizeCityName(city2);
  
  if (norm1 === norm2) return true;
  
  // Verificar si comparten algún código postal
  const postcodes1 = CITY_TO_POSTCODES[norm1] || [];
  const postcodes2 = CITY_TO_POSTCODES[norm2] || [];
  
  return postcodes1.some(pc => postcodes2.includes(pc));
}

/**
 * Expande una zona (nombre de ciudad) a sus códigos postales
 * Útil para la lógica interna de zonas de servicio
 */
export function expandZoneToPostcodes(zoneName: string): string[] {
  // Si ya es un código postal, devolverlo directamente
  if (/^\d{4}$/.test(zoneName)) {
    return [zoneName];
  }
  
  // Intentar como nombre de ciudad
  const postcodes = getPostcodesForCity(zoneName);
  if (postcodes.length > 0) {
    return postcodes;
  }
  
  // No encontrado
  return [];
}
